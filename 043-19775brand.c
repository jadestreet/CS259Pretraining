int
main(int argc, char ** argv)
{
 int a, b, c, len, ind;
 char line[80];

 for (++argv; --argc; ++argv) {
  len = strlen(*argv);

  if (len > 10)
   len = 10;

  for (a = 0; a < 7; ++a) {
   for (b = 0; b < len; ++b) {
    if ((ind = (*argv)[b] - ' ') < 0)
     ind = 0;

    for (c = 0; c < 7; ++c)
     line[8 * b + c] = glyphs[(ind / 8 * 7) + a][(ind % 8 * 7) + c];

    line[8 * b + 7] = ' ';
   }

   for (b = 8 * len - 1; b >= 0; --b) {
    if (line[b] != ' ')
     break;

    line[b] = '\0';
   }

   puts(line);
  }

  puts("");
 }

 return 0;
}
