int N[20] = {23002, 13359, 11466, 64118, 32403, 44024, 63253, 51654, 60960, 45232, 28137, 40242, 27545, 10747, 18543, 32541, 9632, 59878, 43528, 6841};
int len = 20;

int main(){
  int i,j,temp;

  for(i=0; i<len; i++){
    for(j=len-1; j>i; j--){
      if(N[j] < N[j-1]){
        temp = N[j];
        N[j] = N[j-1];
        N[j-1] = temp;
      }
    }
  }

  return 0;
}